// Single line arrow function

// single parameter 
const doubeIt = num => num * 2;

// Multiple parameter 
const add = (x, y) => x + y;

// No parameter 
const nothing = () => 5;

// Multi line  Arrow function 
const doMath = (x, y) => {
    const sum = x + y;
    const diff = x - y;
    const result = sum * diff;
    return result;
}